package com.turn.inscription.bean;

public class InscriptionInventoryDetail {

    private String p;
    private String op;
    private String tick;
    private String max;
    private String lim;
    private String ext;

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public String getOp() {
        return op;
    }

    public void setOp(String op) {
        this.op = op;
    }

    public String getTick() {
        return tick;
    }

    public void setTick(String tick) {
        this.tick = tick;
    }

    public String getMax() {
        return max;
    }

    public void setMax(String max) {
        this.max = max;
    }

    public String getLim() {
        return lim;
    }

    public void setLim(String lim) {
        this.lim = lim;
    }

    public String getExt() {
        return ext;
    }

    public void setExt(String ext) {
        this.ext = ext;
    }
}
